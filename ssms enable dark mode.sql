Enable dark mode ssms

1. go to C:\Program Files (x86)\Microsoft SQL Server Management Studio 18\Common7\IDE 
2. open ssms.pkgundef 
3. find // Remoce Dark Theme
4. add two // before the command of root key and save the file
5. Menu >> Tools >> Options >> Environment >> General and see the color theme, 
		you will notice that it will have an option for the dark theme. Select this option and click on Save.